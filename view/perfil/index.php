function datos($usuario,$variable)
{   
    try {
        //code...
        if(empty($usuario[$variable]))
        {
            return "Aun no Asigando";
        }
        else
        {
            if(validateDate($usuario[$variable]))
            {
                $date = date_create($usuario[$variable]);
                $fecha=date_format($date, 'Y-m-d');
                return $fecha;
            }
            else
            {
                return $usuario[$variable];
            }
        }
    } catch (\Exception $th) {
        //throw $th;
        $incidencia=new incidencias(new Connexion);
        if(isset($usuario))
        {
            $incidencia->setfkusuario($usuario['pk_usuario']);
        }
        $incidencia->setname($th);
        $date=new DateTime();
        $datetime=$date->format('Y-m-d H:i:s');
        $incidencia->setcreated_at($datetime);
        $incidencia->insert();
    }
  
}
function validateDate($date, $format = 'Y-m-d H:i:s')
{
    $d = DateTime::createFromFormat($format, $date);
    return $d && $d->format($format) == $date;
}
